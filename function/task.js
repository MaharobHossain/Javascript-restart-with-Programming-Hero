//Take four parameters. Multiply the four numbers and then return the result.


function multiplyNumbers(num1, num2, num3, num4) {
    const multiple = num1 * num2 * num3 * num4;
    return multiple;
}

console.log(multiplyNumbers(9, 5, 4, 7)); 
